/**
 * Validates document content against legal safety guidelines
 */
export function validateLegalContent(
    content: string,
    documentType: string
): { isValid: boolean; violations: string[] } {
    const violations: string[] = [];

    // Check for restricted topics
    const lowerContent = content.toLowerCase();
    for (const topic of LEGAL_SAFEGUARDS.restrictedTopics) {
        if (lowerContent.includes(topic)) {
            violations.push(`Content mentions restricted topic: "${topic}"`);
        }
    }

    // Check for mandatory elements
    const mandatoryForType = LEGAL_SAFEGUARDS.mandatoryElements[
        documentType as keyof typeof LEGAL_SAFEGUARDS.mandatoryElements
    ];

    if (mandatoryForType) {
        for (const element of mandatoryForType) {
            const elementCheck = element.replace(/_/g, ' ');
            if (!lowerContent.includes(elementCheck)) {
                // Only warn, don't fail validation
                violations.push(`Warning: Missing recommended element: "${elementCheck}"`);
            }
        }
    }

    // Check for required language patterns
    let hasGoverningSectionOrLaw = false;

    if (
        lowerContent.includes('governing law') ||
        lowerContent.includes('jurisdiction')
    ) {
        hasGoverningSectionOrLaw = true;
    }

    if (
        lowerContent.includes('disclaimer') ||
        lowerContent.includes('important') ||
        lowerContent.includes('legal advice')
    ) {
        // Has disclaimer/warning - good
    }

    if (!hasGoverningSectionOrLaw) {
        violations.push('Warning: No governing law or jurisdiction clause found');
    }

    return {
        isValid: violations.every((v) => v.startsWith('Warning:')),
        violations,
    };
}

/**
 * Gets the appropriate disclaimer for a document type
 */
export function getDisclaimer(documentType: string): string {
    return (
        LEGAL_DISCLAIMERS[
        documentType as keyof typeof LEGAL_DISCLAIMERS
        ] || LEGAL_DISCLAIMERS.general
    );
}

